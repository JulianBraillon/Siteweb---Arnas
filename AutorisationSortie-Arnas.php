    <h1>Autorisation de Sortie du Territoire (AST) pour les Mineurs :</h1>
<p>Depuis le 15 janvier 2017, tout mineur résidant habituellement en France et voyageant à l'étranger sans être accompagné d'un titulaire de l'autorité parentale doit être en possession d'une Autorisation de Sortie du Territoire (AST). Cette mesure vise à renforcer la sécurité des enfants lors de leurs déplacements internationaux.</p>
<br><br>

<h2>Conditions d'Éligibilité :</h2>
<p>L'AST est requise pour :</p>
<ul>
    <li>Les mineurs français résidant en France.</li>
    <li>Les mineurs étrangers résidant habituellement en France.</li>
</ul>
<p>Elle est exigible pour toute sortie du territoire français, que ce soit pour un voyage individuel ou collectif (voyage scolaire, séjour linguistique, etc.), dès lors que le mineur voyage sans un titulaire de l'autorité parentale.</p>
<br><br>

<h2>Procédure de Demande :</h2>
<p>La demande d'AST s'effectue en ligne via le téléservice officiel :</p>
<p><a href="https://www.service-public.fr/particuliers/vosdroits/F1359">https://www.service-public.fr/particuliers/vosdroits/F1359</a></p>
<p>Une fois le formulaire renseigné, vous pourrez le télécharger et l'imprimer. Il n'est pas nécessaire de se déplacer en mairie ou en préfecture.</p>
<br><br>

<h2>Documents à Fournir :</h2>
<ul>
    <li>Le formulaire Cerfa n°15646*01 dûment rempli et signé par l'un des parents titulaires de l'autorité parentale.</li>
    <li>Une photocopie lisible de la pièce d'identité du parent signataire (carte d'identité ou passeport).</li>
</ul>
<p>Le mineur doit également être en possession de :</p>
<ul>
    <li>Une pièce d'identité valide à son nom (carte d'identité ou passeport).</li>
    <li>Un visa, si nécessaire, en fonction des exigences du pays de destination.</li>
</ul>
<br><br>

<h2>Durée de Validité de l'AST :</h2>
<p>La durée de l'AST est fixée par le parent signataire. Elle peut correspondre à la durée d'un voyage spécifique ou être valable pour une période déterminée (par exemple, 6 mois). Toutefois, la durée maximale de validité ne peut excéder un an.</p>
<br><br>

<h2>Contrôle aux Frontières :</h2>
<p>Le formulaire d'AST doit être présenté au garde-frontière lors du contrôle à la frontière. Il est recommandé de conserver une copie de l'AST et des pièces justificatives pendant toute la durée du voyage.</p>
<br><br>

<h2>Cas Particuliers :</h2>
<p><strong>Parents Divorcés :</strong> En cas d'autorité parentale conjointe, l'AST peut être signée par un seul parent. Toutefois, il est conseillé d'obtenir l'accord de l'autre parent pour éviter tout conflit.</p>
<p><strong>Opposition ou Interdiction de Sortie du Territoire :</strong> Si le mineur fait l'objet d'une opposition ou d'une interdiction de sortie du territoire, l'AST devra être signée par les deux parents. Chacun devra autoriser le mineur à quitter le territoire par le biais d'une déclaration devant un officier de police judiciaire, au plus tard 5 jours avant la date envisagée de départ, sauf en cas de circonstances exceptionnelles dûment justifiées.</p>
