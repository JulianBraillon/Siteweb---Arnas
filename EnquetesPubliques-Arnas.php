    <h1>Enquêtes Publiques :</h1>
<p>Les enquêtes publiques sont des procédures réglementaires permettant d'informer les citoyens et de recueillir leurs avis sur des projets susceptibles d'avoir un impact sur l'environnement, l'aménagement du territoire ou la vie locale. Elles garantissent la transparence et la participation du public dans le processus décisionnel.</p>
<br><br>

<h2>Consultation des Enquêtes en Cours :</h2>
<p>Les enquêtes publiques en cours concernant la commune d'Arnas sont consultables en ligne sur les plateformes suivantes :</p>
<ul>
    <li><a href="https://www.notre-territoire.com/enquetes-publiques/Arnas-69400">Notre Territoire - Enquêtes Publiques Arnas</a></li>
    <li><a href="https://www.registre-numerique.fr/pluih-rlpi-agglo-villefranche">Registre Numérique - PLUiH Villefranche Beaujolais Saône</a></li>
</ul>
<p>Ces plateformes permettent de consulter les dossiers, de formuler des observations et de suivre l'évolution des procédures.</p>
<br><br>

<h2>Exemples d'Enquêtes Récentes :</h2>
<ul>
    <li><strong>Plan Local d'Urbanisme intercommunal et de l'Habitat (PLUiH) :</strong> Enquête publique menée du 10 février au 14 mars 2025, incluant une permanence en mairie le 22 février 2025 de 10h à 12h. <a href="https://station.illiwap.com/fr/public/%4069013/actu/enquete-publique-pluih-du-10-fevrier-au-14-mars-1-1">Détails de l'enquête</a></li>
    <li><strong>Extension d'une carrière alluvionnaire par la société GRANULATS VICAT :</strong> Enquête publique réalisée du 14 novembre au 16 décembre 2022. <a href="https://www.rhone.gouv.fr/Actions-de-l-Etat/Environnement-developpement-durable-risques-naturels-et-technologiques/Installations-classees-pour-la-protection-de-l-environnement/Enquetes-publiques-et-consultations-du-public-par-voie-electronique-procedure-autorisation">Informations supplémentaires</a></li>
</ul>
<br><br>

<h2>Participation du Public :</h2>
<p>Les citoyens peuvent participer aux enquêtes publiques en :</p>
<ul>
    <li>Consultant les dossiers disponibles en mairie ou en ligne.</li>
    <li>Formulant des observations écrites sur les registres prévus à cet effet.</li>
    <li>Assistant aux permanences des commissaires enquêteurs pour poser des questions ou exprimer des avis.</li>
</ul>
<p>Les observations recueillies sont prises en compte dans le rapport final du commissaire enquêteur, influençant ainsi les décisions finales.</p>
