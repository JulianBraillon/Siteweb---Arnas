    <h1>Carte Grise (Certificat d’Immatriculation) :</h1>
<p>Depuis le 6 novembre 2017, les guichets en préfecture pour les demandes de carte grise sont définitivement fermés. Toutes les démarches doivent désormais être effectuées en ligne ou auprès de professionnels habilités.</p>
<br><br>

<h2>Effectuer votre demande en ligne :</h2>
<p>Vous pouvez réaliser vos démarches relatives à la carte grise sur le site officiel de l'ANTS (Agence Nationale des Titres Sécurisés) :</p>
<ul>
    <li><a href="https://immatriculation.ants.gouv.fr/">https://immatriculation.ants.gouv.fr/</a></li>
</ul>
<p>Sur ce portail, vous pourrez :</p>
<ul>
    <li>Immatriculer un véhicule neuf ou d'occasion</li>
    <li>Déclarer la vente ou le don de votre véhicule</li>
    <li>Demander un duplicata en cas de perte, vol ou détérioration</li>
    <li>Modifier l'adresse sur votre certificat d'immatriculation</li>
    <li>Suivre l'état d'avancement de votre demande</li>
</ul>
<br><br>

<h2>Professionnels habilités à Arnas :</h2>
