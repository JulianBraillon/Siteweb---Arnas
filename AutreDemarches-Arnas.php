    <h1>Autres Démarches Administratives :</h1>
<p>La mairie d'Arnas propose un ensemble de services administratifs pour accompagner les citoyens dans leurs démarches quotidiennes. Ces services couvrent divers domaines tels que l'état civil, l'urbanisme, les élections, et bien d'autres.</p>
<br><br>

<h2>État Civil :</h2>
<p>Le service de l'état civil de la mairie d'Arnas est à votre disposition pour :</p>
<ul>
    <li>La déclaration de naissance.</li>
    <li>La déclaration de décès.</li>
    <li>La célébration de mariages.</li>
    <li>La délivrance d'actes d'état civil (naissance, mariage, décès).</li>
</ul>
<p>Pour toute demande, veuillez vous présenter en mairie muni des pièces justificatives nécessaires.</p>
<br><br>

<h2>Urbanisme :</h2>
<p>Le service urbanisme vous accompagne dans vos projets de construction, de rénovation ou d'aménagement. Les démarches incluent :</p>
<ul>
    <li>La demande de permis de construire.</li>
    <li>La déclaration préalable de travaux.</li>
    <li>Le certificat d'urbanisme.</li>
</ul>
<p>Il est recommandé de prendre rendez-vous avec le service urbanisme pour discuter de votre projet et obtenir les formulaires appropriés.</p>
<br><br>

<h2>Élections :</h2>
<p>Pour participer aux élections, vous devez être inscrit sur les listes électorales de la commune. Les démarches incluent :</p>
<ul>
    <li>L'inscription sur les listes électorales.</li>
    <li>La demande de procuration de vote.</li>
    <li>La mise à jour de votre situation électorale.</li>
</ul>
<p>Les inscriptions peuvent se faire en ligne via le site <a href="https://www.service-public.fr">service-public.fr</a> ou directement en mairie.</p>
<br><br>

<h2>Autres Services :</h2>
<p>La mairie d'Arnas propose également d'autres services administratifs tels que :</p>
<ul>
    <li>La délivrance de certificats de vie.</li>
    <li>La légalisation de signature.</li>
    <li>La certification conforme de documents.</li>
</ul>
<p>Pour ces démarches, veuillez vous présenter en mairie avec une pièce d'identité et les documents concernés.</p>

