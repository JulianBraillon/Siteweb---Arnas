    <h1>Subvention pour l'Achat d'un Vélo :</h1>
<p>La commune d'Arnas propose une aide financière pour l'acquisition d'un vélo, qu'il soit électrique ou non. Cette initiative vise à encourager les déplacements doux et à réduire l'empreinte carbone des habitants.</p>
<br><br>

<h2>Conditions d'Éligibilité :</h2>
<p>Pour bénéficier de cette subvention, les critères suivants doivent être remplis :</p>
<ul>
    <li>Résider à Arnas.</li>
    <li>Être majeur.</li>
    <li>Ne pas avoir déjà bénéficié de cette aide au cours de l'année en cours.</li>
</ul>
<p>Il n'y a plus de condition de ressources pour cette aide, et elle est désormais ouverte à l'achat de vélos non électriques, en plus des vélos électriques.</p>
<br><br>

<h2>Montant de la Subvention :</h2>
<p>La commune a dégagé un budget de 5 000 € pour l'année, correspondant à 50 financements de 100 € chacun. Une fois ce budget atteint, la campagne est clôturée.</p>
<br><br>

<h2>Procédure de Demande :</h2>
<p>Pour solliciter la subvention, vous devez :</p>
<ul>
    <li>Remplir le formulaire de demande disponible en mairie ou en téléchargement sur le site officiel de la commune.</li>
    <li>Fournir une attestation sur l'honneur précisant que vous n'avez pas déjà bénéficié de cette aide au cours de l'année en cours.</li>
    <li>Présenter une copie de la facture d'achat du vélo.</li>
</ul>
<p>Les dossiers complets sont à déposer à la mairie d'Arnas, Service Vie Associative, ou à envoyer par courrier à :</p>
<p>
    Mairie d'Arnas<br>
    Service Vie Associative<br>
    1 Place de la Mairie<br>
    69400 Arnas
</p>
<br><br>

<h2>Contact :</h2>
