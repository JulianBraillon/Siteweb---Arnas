    <h1>Pacte Civil de Solidarité (PACS) :</h1>
<p>Le Pacte Civil de Solidarité (PACS) est une forme d'union civile permettant à deux personnes majeures, de sexe différent ou de même sexe, d'organiser leur vie commune. Il offre un cadre juridique souple, distinct du mariage, tout en établissant des droits et des devoirs en matière de soutien matériel, de logement, de patrimoine, d'impôts et de droits sociaux.</p>
<br><br>

<h2>Conditions d'Éligibilité :</h2>
<p>Pour conclure un PACS, les conditions suivantes doivent être remplies :</p>
<ul>
    <li>Être majeur (18 ans ou plus).</li>
    <li>Ne pas être déjà marié(e) ou pacsé(e).</li>
    <li>Ne pas avoir de lien de parenté ou d'alliance direct avec l'autre partenaire (ascendants, descendants, alliés en ligne directe, collatéraux jusqu'au 3<sup>e</sup> degré inclus).</li>
    <li>Résider en France ou, si résidant à l'étranger, être de nationalité française ou justifier d'une résidence légale en France.</li>
</ul>
<br><br>

<h2>Procédure de Conclusion :</h2>
<p>Depuis novembre 2017, l'enregistrement d'un PACS se fait en mairie. La procédure comprend :</p>
<ul>
    <li>La rédaction d'une convention de PACS, rédigée soit par les partenaires eux-mêmes, soit par un notaire.</li>
    <li>La déclaration conjointe de PACS, accompagnée des pièces justificatives requises (actes de naissance, justificatifs de domicile, etc.).</li>
    <li>Le dépôt du dossier complet en personne à la mairie de la résidence commune.</li>
</ul>
<p>Il est recommandé de prendre rendez-vous auprès du service de l'état civil de la mairie pour connaître les documents exacts à fournir et les modalités de dépôt du dossier.</p>
<br><br>

<h2>Coût de la Procédure :</h2>
<p>L'enregistrement d'un PACS en mairie est gratuit. Toutefois, si la convention est rédigée par un notaire, des frais peuvent s'appliquer, généralement entre 300 et 400 euros, selon le notaire et la complexité de la convention rédigée. Il est conseillé de se renseigner directement auprès des notaires pour obtenir un devis précis.</p>
<br><br>

<h2>Droits et Obligations des Partenaires :</h2>
<p>Le PACS établit des droits et des devoirs réciproques entre les partenaires :</p>
<ul>
    <li><strong>Solidarité financière :</strong> Obligation de contribuer aux charges du ménage selon ses capacités respectives.</li>
    <li><strong>Patrimoine :</strong> Possibilité de choisir entre deux régimes patrimoniaux : la séparation de biens ou l'indivision.</li>
    <li><strong>Fiscalité :</strong> Imposition commune possible après un an de vie commune.</li>
    <li><strong>Protection sociale :</strong> Couverture sociale du partenaire survivant en cas de décès, sous certaines conditions.</li>
</ul>
<p>Il est important de noter que le PACS ne confère pas les mêmes droits que le mariage, notamment en matière de succession, de pension de réversion ou d'autorité parentale.</p>
<br><br>

<h2>Dissolution du PACS :</h2>
<p>Le PACS peut être dissous à tout moment, d'un commun accord entre les partenaires ou unilatéralement par l'un d'eux. La procédure de dissolution se fait également en mairie, avec la remise d'une déclaration conjointe ou d'une déclaration unilatérale, selon le cas. Il est conseillé de se renseigner auprès du service de l'état civil pour connaître les démarches précises à suivre.</p>

