    <h1>Arrêtés Préfectoraux :</h1>
<p>Les arrêtés préfectoraux sont des actes administratifs pris par le préfet du département du Rhône. Ils s'appliquent sur l'ensemble du territoire départemental, y compris la commune d'Arnas, et concernent divers domaines tels que la sécurité publique, l'environnement, la santé, et l'organisation des services publics.</p>
<br><br>

<h2>Consultation des Arrêtés :</h2>
<p>Les arrêtés préfectoraux en vigueur sont disponibles en ligne sur le site officiel de la commune d'Arnas. Vous pouvez les consulter en accédant à la rubrique dédiée :</p>
<p><a href="https://www.arnas.fr/fr/information/113448/publications-reglementaires">https://www.arnas.fr/fr/information/113448/publications-reglementaires</a></p>
<p>Cette page regroupe les publications réglementaires, y compris les arrêtés municipaux et préfectoraux en cours.</p>
<br><br>

<h2>Domaines Concernés :</h2>
<p>Les arrêtés préfectoraux peuvent porter sur :</p>
<ul>
    <li>La réglementation de la circulation et du stationnement.</li>
    <li>Les mesures de sécurité publique et de prévention des risques.</li>
    <li>La protection de l'environnement et de la santé publique.</li>
    <li>La gestion des crises sanitaires ou climatiques.</li>
    <li>L'organisation des élections et des services publics.</li>
</ul>
<br><br>

<h2>Affichage et Accessibilité :</h2>
<p>Conformément à la législation en vigueur, les arrêtés préfectoraux sont affichés en mairie pour assurer leur publicité. Ils sont également disponibles en ligne pour faciliter leur consultation par les administrés.</p>
