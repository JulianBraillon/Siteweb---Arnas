    <h1>Subventions Communales :</h1>
<p>La commune d'Arnas soutient activement les initiatives locales en proposant diverses subventions destinées à encourager la vie associative, les mobilités douces et les projets citoyens. Ces aides financières visent à renforcer le dynamisme du territoire et à accompagner les acteurs locaux dans leurs démarches.</p>
<br><br>

<h2>Subventions aux Associations :</h2>
<p>Les associations arnasiennes peuvent solliciter une subvention municipale pour soutenir leurs activités. Un règlement d'attribution des subventions est mis à disposition, précisant les critères d'éligibilité, les modalités de demande et les pièces justificatives requises.</p>
<p>Pour plus d'informations et pour accéder aux documents nécessaires, veuillez consulter la page dédiée :</p>
<p><a href="https://www.arnas.fr/fr/information/114425/subvention-associations">https://www.arnas.fr/fr/information/114425/subvention-associations</a></p>
<br><br>

<h2>Subvention pour l'Achat de Vélo :</h2>
<p>Dans le cadre de sa politique en faveur des mobilités douces, la commune propose une aide financière pour l'acquisition d'un vélo. Le budget alloué permet de financer jusqu'à 50 demandes par an, pour un montant total de 5 000 €.</p>
<p>Les documents nécessaires, tels que l'attestation sur l'honneur et le formulaire de demande, sont disponibles sur la page suivante :</p>
<p><a href="https://www.arnas.fr/fr/information/113501/subvention-velo">https://www.arnas.fr/fr/information/113501/subvention-velo</a></p>
<br><br>

<h2>Autres Subventions :</h2>
<p>La commune peut également attribuer des subventions ponctuelles pour soutenir des projets spécifiques ou des initiatives citoyennes. Les modalités d'attribution et les critères d'éligibilité sont définis au cas par cas.</p>
<p>Pour toute demande particulière, il est recommandé de prendre contact directement avec la mairie.</p>
