    <div class="body">
        <h1>Demande de Carte Nationale d'Identité (CNI) :</h1>
        <p>Toute personne de nationalité française, sans condition d'âge, peut solliciter l'établissement d'une carte nationale d'identité.</p>
        <br><br>
        <h2>Où effectuer la demande ?</h2>  
        <p>La commune d'Arnas n'est pas habilitée à délivrer les cartes d'identité. Il est donc nécessaire de se rendre dans une mairie équipée d'un dispositif de recueil (DR), telle que celle de Gleizé. Les demandes y sont acceptées du lundi au vendredi, sur rendez-vous. </p> 
        <br><br>
        <h2>Pièces à fournir :</h2>
        <p>Les documents requis varient selon votre situation (première demande, renouvellement, perte ou vol). De manière générale, prévoyez :
            Une photo d'identité récente et conforme aux normes.
            Un justificatif de domicile de moins de 3 mois.
            Un acte de naissance (sauf si la mairie peut accéder à vos données via le dispositif COMEDEC).
            L'ancienne carte d'identité (en cas de renouvellement).
            Un timbre fiscal (en cas de perte ou de vol)</p>
        <br><br>
        <h2>Délai de traitement :</h2>
        <p>Les délais de fabrication peuvent varier en fonction de la période et de l'affluence. Il est recommandé de s'y prendre plusieurs semaines à l'avance.</p>
        <br><br>
        <h2>Conseils pratiques :</h2>
        <p>Effectuez une pré-demande en ligne sur le site de l'ANTS (Agence Nationale des Titres Sécurisés) pour gagner du temps lors du rendez-vous.   
            Assurez-vous que tous vos documents sont à jour et conformes aux exigences.
            Prenez rendez-vous suffisamment tôt, surtout en période de forte demande (vacances scolaires, rentrée, etc.).</p>
        <br>
        <p>Pour toute question ou assistance supplémentaire, n'hésitez pas à contacter la mairie d'Arnas</p>
