    <h1>Demande de Passeport :</h1>
<p>Toute personne de nationalité française, sans condition d'âge, peut solliciter l'établissement d'un passeport biométrique.</p>
<br><br>

<h2>Où effectuer la demande ?</h2>
<p>La commune d'Arnas n'est pas habilitée à délivrer les passeports. Il est donc nécessaire de se rendre dans une mairie équipée d’un dispositif de recueil (DR), comme celle de Gleizé.</p>
<p>Les demandes y sont acceptées du lundi au vendredi, uniquement sur rendez-vous. Il est conseillé de réserver en ligne ou par téléphone à l’avance.</p>
<br><br>

<h2>Pièces à fournir :</h2>
<p>Les documents nécessaires varient selon votre situation (première demande, renouvellement, perte ou vol). En général, il vous faudra :</p>
<ul>
    <li>Une photo d'identité récente et conforme aux normes.</li>
    <li>Un justificatif de domicile de moins de 3 mois.</li>
    <li>Un acte de naissance (non requis si la mairie peut vérifier via COMEDEC).</li>
    <li>L’ancien passeport (en cas de renouvellement).</li>
    <li>Un timbre fiscal (obligatoire en cas de perte ou de vol).</li>
</ul>
<br><br>

<h2>Délai de traitement :</h2>
<p>Les délais de traitement peuvent varier selon les périodes de l'année. Il est fortement recommandé d’anticiper votre demande, notamment avant les vacances scolaires ou l'été.</p>
<br><br>

<h2>Conseils pratiques :</h2>
<ul>
    <li>Effectuez une pré-demande en ligne sur le site de l’ANTS (Agence Nationale des Titres Sécurisés) pour gagner du temps.</li>
    <li>Vérifiez que tous vos documents sont à jour et conformes aux normes en vigueur.</li>
    <li>Planifiez votre rendez-vous dès que possible pour éviter les délais prolongés.</li>
</ul>
<br>

<p>Pour toute question ou assistance supplémentaire, n’hésitez pas à contacter la mairie d’Arnas.</p>
